// constantes
const a = 10;
const b = 7;
let result;
// adição
result = a + b;
console.log("Adição: " + result);
// subtração
result = a - b;
console.log("Subtração: " + result);
// multiplicação
result = a * b;
console.log("Multiplicação: " + result);
// divisão
result = a / b;
console.log("Divisão: " + result);
// módulo(resto)
result = a % b;
console.log("Módulo(resto): " + result);